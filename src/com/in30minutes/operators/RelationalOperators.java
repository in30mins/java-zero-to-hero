package com.in30minutes.operators;

public class RelationalOperators {
    public static void main(String[] args) {
        byte a = 23;
        byte b = 21;
        System.out.println(a==b);
        System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a<b);
        System.out.println(a<=b);
        System.out.println(a>=b);
    }
}
