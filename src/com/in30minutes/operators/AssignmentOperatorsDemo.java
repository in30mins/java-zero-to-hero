package com.in30minutes.operators;

public class AssignmentOperatorsDemo {
    public static void main(String[] args) {
        byte a = 23;
        byte b = 34;
        System.out.println(a+=b);
        System.out.println(a-=b);
        System.out.println(a*=b);
        System.out.println(a/=b);

    }
}
